//program to display 5time hello on screen.

#include<stdio.h>

//Demonstration of ITERATION. 

void Display()			//defination
{
	int iCnt=0;
	
	//1 initialization
	//2 condition
	//3 displacement 
	//4 loop body
	
	//	1			2		3
	for(iCnt=1; iCnt<=5; iCnt++)
	{
		printf("Hello \n");				//4
	}
	
}
int main()
{
	Display();			//function call
	
	return 0;
}
